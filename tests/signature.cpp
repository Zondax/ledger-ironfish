TEST(Signature, Signing) {
    uint8_t blob[8000] = {0};
    for (const auto &testcase : Signature::signatureTestvectors) {
        memset(blob, 0, sizeof(blob));

        uint8_t spendingKey[KEY_LENGTH] = {0};
        parseHexString(spendingKey, sizeof(spendingKey), testcase.spendingKey.c_str());
        const uint16_t blobLen = parseHexString(blob, sizeof(blob), testcase.blob.c_str());

        // Parse the transaction and verify the transaction hash
        parser_tx_t txObj;
        parser_context_t ctx = {0};
        ASSERT_EQ(parser_parse(&ctx, blob, blobLen, &txObj), parser_ok);

        uint8_t txnHash[HASH_LEN] = {0};
        ASSERT_EQ(transaction_signature_hash(&txObj, txnHash), parser_ok);
        EXPECT_EQ(toHexString(txnHash, HASH_LEN), testcase.txnHash);

        // Sign process
        // Derive ask and then randomized private key
        uint8_t ask[KEY_LENGTH] = {0};
        ASSERT_EQ(convertKey(spendingKey, MODIFIER_ASK, ask, true), parser_ok);
        uint8_t randomizedPrivateKey[KEY_LENGTH] = {0};
        ASSERT_EQ(randomizeKey(ask, txObj.publicKeyRandomness.ptr, randomizedPrivateKey), parser_ok);

        // Random buffer[80] that will be used to generate the signature
        uint8_t rng[RNG_LEN] = {0};
        parseHexString(rng, sizeof(rng), testcase.rng.c_str());

        // Sign the transaction
        uint8_t signature[REDJUBJUB_SIGNATURE_LEN] = {0};
        ASSERT_EQ(crypto_signRedjubjub(randomizedPrivateKey, rng, txnHash, signature), zxerr_ok);
        EXPECT_EQ(toHexString(signature, REDJUBJUB_SIGNATURE_LEN), testcase.signature);
    }
}

